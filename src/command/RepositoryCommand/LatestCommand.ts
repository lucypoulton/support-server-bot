import {AbstractRepositoryCommand} from "./AbstractRepositoryCommand";

export class LatestCommand extends AbstractRepositoryCommand {
    get name(): string {
        return "latest";
    }

    protected execChannelAction(repo: string, args?: string[]): string | null {
        return `${repo}/releases/latest`;
    }
}